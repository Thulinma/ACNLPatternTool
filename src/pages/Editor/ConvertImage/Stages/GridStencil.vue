<template>
  <div :class="classes.stencil" :style="style">
    <bounding-box
      :width="stencilCoordinates.width"
      :height="stencilCoordinates.height"
      :transitions="transitions"
      :class="classes.boundingBox"
      :handlers="handlers"
      :handlers-component="handlersComponent"
      :handlers-classes="handlersClasses"
      :handlers-wrappers-classes="handlersWrappersClasses"
      :lines="lines"
      :lines-component="linesComponent"
      :lines-classes="linesClasses"
      :lines-wrappers-classes="linesWrappersClasses"
      :resizable="resizable"
      @resize="onResize"
      @resize-end="onResizeEnd"
    >
      <draggable-area :movable="movable" @move="onMove" @move-end="onMoveEnd">
        <stencil-preview
          :image="image"
          :coordinates="coordinates"
          :width="stencilCoordinates.width"
          :height="stencilCoordinates.height"
          :class="classes.preview"
          :transitions="transitions"
        />
      </draggable-area>
      <!-- The grid to preview the tiled areas -->
      <div class="grid"
        :style="{
          'grid-template-rows': `repeat(${rows}, 1fr)`,
        }"
      >
        <div class="grid-row"
          v-for="r in rows"
          :key="r"
          :style="{
            'grid-template-columns': `repeat(${columns}, 1fr)`,
            'border-top-style': r ? 'solid': 'none',
            'border-bottom-style': r === rows ? 'solid': 'none',
          }"
        >
          <div
            v-for="c in columns"
            :key="c"
            class="grid-cell"
            :style="{
              'border-left-style': c ? 'solid' : 'none',
              'border-right-style': c === columns ? 'solid' : 'none',
            }"
          >
          </div>
        </div>
      </div>
    </bounding-box>
  </div>
</template>

<script>
// NOTE: DON'T CONVERT TO TS, CUSTOM 3RD PARTY COMPONENT
import classnames from "classnames";
import bem from "easy-bem";
import {
  BoundingBox,
  DraggableArea,
  StencilPreview,
} from "vue-advanced-cropper";
import { SimpleHandler } from "vue-advanced-cropper";
import { SimpleLine } from "vue-advanced-cropper";

const cn = bem("vue-rectangle-stencil");

export default {
  name: "GridStencil",
  components: {
    StencilPreview,
    BoundingBox,
    DraggableArea,
  },
  props: {
    image: {
      type: Object,
    },
    coordinates: {
      type: Object,
    },
    stencilCoordinates: {
      type: Object,
    },
    handlers: {
      type: Object,
    },
    handlersComponent: {
      type: [Object, String],
      default() {
        return SimpleHandler;
      },
    },
    lines: {
      type: Object,
    },
    linesComponent: {
      type: [Object, String],
      default() {
        return SimpleLine;
      },
    },
    aspectRatio: {
      type: [Number, String],
    },
    minAspectRatio: {
      type: [Number, String],
    },
    maxAspectRatio: {
      type: [Number, String],
    },
    movable: {
      type: Boolean,
      default: true,
    },
    resizable: {
      type: Boolean,
      default: true,
    },
    transitions: {
      type: Object,
    },
    movingClass: {
      type: String,
    },
    resizingClass: {
      type: String,
    },
    previewClass: {
      type: String,
    },
    boundingBoxClass: {
      type: String,
    },
    linesClasses: {
      type: Object,
      default() {
        return {};
      },
    },
    linesWrappersClasses: {
      type: Object,
      default() {
        return {};
      },
    },
    handlersClasses: {
      type: Object,
      default() {
        return {};
      },
    },
    handlersWrappersClasses: {
      type: Object,
      default() {
        return {};
      },
    },
    rows: {
      type: Number,
      default: () => 1,
    },
    columns: {
      type: Number,
      default: () => 1,
    },
  },
  data() {
    return {
      moving: false,
      resizing: false,
    };
  },
  computed: {
    classes() {
      return {
        stencil: classnames(
          cn({
            movable: this.movable,
            moving: this.moving,
            resizing: this.resizing,
          }),
          this.moving && this.movingClass,
          this.resizing && this.resizingClass
        ),
        preview: classnames(cn("preview"), this.previewClass),
        boundingBox: classnames(cn("bounding-box"), this.boundingBoxClass),
      };
    },
    style() {
      const { height, width, left, top } = this.stencilCoordinates;

      const style = {
        width: `${width}px`,
        height: `${height}px`,
        transform: `translate(${left}px, ${top}px)`,
      };

      if (this.transitions && this.transitions.enabled) {
        style.transition = `${this.transitions.time}ms ${this.transitions.timingFunction}`;
      }
      return style;
    },
  },
  methods: {
    onMove(moveEvent) {
      this.$emit("move", moveEvent);
      this.moving = true;
    },
    onMoveEnd() {
      this.$emit("move-end");
      this.moving = false;
    },
    onResize(resizeEvent) {
      this.$emit("resize", resizeEvent);
      this.resizing = true;
    },
    onResizeEnd() {
      this.$emit("resize-end");
      this.resizing = false;
    },
    aspectRatios() {
      return {
        minimum: this.aspectRatio || this.minAspectRatio,
        maximum: this.aspectRatio || this.maxAspectRatio,
      };
    },
  },
};
</script>

